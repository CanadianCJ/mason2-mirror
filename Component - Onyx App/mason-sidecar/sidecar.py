# ROUTES
# -------------------------
@app.get("/health")
def health():
    return {"ok": True, "auto": STATE["auto"], "last_scan": STATE["last_scan"], "auto_apply_safe": STATE["auto_apply_safe"]}

@app.post("/auto/start")
def auto_start(b: StartBody):
    STATE["roots"] = [str(Path(r).resolve()) for r in b.roots]
    STATE["interval"] = int(b.interval_sec or 600)
    STATE["auto"] = True
    threading.Thread(target=auto_loop, daemon=True).start()
    return {"ok": True, "watching": STATE["roots"]}

@app.post("/auto/stop")
def auto_stop():
    STATE["auto"] = False
    return {"ok": True}

@app.post("/auto/config")
def auto_config(b: AutoConfigBody):
    if b.auto_apply_safe is not None:
        STATE["auto_apply_safe"] = bool(b.auto_apply_safe)
    if b.interval_sec is not None:
        STATE["interval"] = int(b.interval_sec)
    return {"ok": True, "auto_apply_safe": STATE["auto_apply_safe"], "interval_sec": STATE["interval"]}

@app.post("/scan")
def scan_now(b: ScanBody):
    return do_scan(b.roots)

@app.get("/proposals")
def proposals():
    return {"ok": True, "count": len(STATE["proposals"]), "items": STATE["proposals"]}

@app.post("/apply/safe")
def apply_safe(b: ApplyBody):
    return apply_safe_core(ids=b.ids, target_root=b.target_root)

@app.get("/audit/tail")
def audit_tail(lines: int = 50):
    if not _audit_enabled():
        return {"ok": False, "error": "audit not configured"}
    ap = _audit_path()
    if not ap.exists():
        return {"ok": True, "lines": []}
    try:
        with ap.open("r", encoding="utf-8") as f:
            buff = f.readlines()[-max(1, min(500, int(lines))):]
        return {"ok": True, "lines": buff}
    except Exception as e:
        raise HTTPException(status_code=500, detail=str(e))

@app.get("/bridge/status")
def bridge_status():
    return {
        "ok": True,
        "configured": BRIDGE["configured"],
        "assistant": (BRIDGE["cfg"].get("assistant") if BRIDGE["cfg"] else None),
        "forbidden": (BRIDGE["cfg"].get("guardrails",{}).get("forbidden",[]) if BRIDGE["cfg"] else []),
        "review": (BRIDGE["cfg"].get("review",{}) if BRIDGE["cfg"] else {}),
        "audit_file": (BRIDGE["cfg"].get("audit",{}).get("log_file") if BRIDGE["cfg"] else None),
    }

@app.post("/bridge/config")
def bridge_config(b: BridgeConfigBody):
    cfg_path = Path(b.path).expanduser().resolve()
    if not cfg_path.exists():
        return {"ok": False, "error": f"config not found: {cfg_path}"}
    if yaml is None:
        return {"ok": False, "error": "pyyaml not installed"}
    data = yaml.safe_load(cfg_path.read_text(encoding="utf-8"))
    BRIDGE["configured"] = True
    BRIDGE["config_path"] = str(cfg_path)
    BRIDGE["cfg"] = data or {}
    audit_log("bridge_configured", {"path": str(cfg_path)})
    return {"ok": True, "path": str(cfg_path)}

# NEW: Watchdog routes
@app.post("/watch/config")
def watch_config(b: WatchConfigBody):
    WATCH["services"] = []
    for s in b.services:
        WATCH["services"].append({
            "name": s.name,
            "url": s.url,
            "expect_code": int(s.expect_code),
            "interval_sec": int(s.interval_sec),
            "next_due": 0.0,
            "last_status": None,
            "last_error": None,
            "last_time": None,
            "consecutive_failures": 0,
        })
    WATCH["proposals"] = []
    audit_log("watch_config", {"count": len(WATCH["services"])})
    return {"ok": True, "count": len(WATCH["services"])}

@app.get("/watch/status")
def watch_status():
    return {
        "ok": True,
        "services": WATCH["services"],
        "proposals": len(WATCH.get("proposals", [])),
    }

# -------------------------
# AUTO LOOP
# -------------------------
def auto_loop():
    while STATE["auto"]:
        try:
            # Main scan cycle
            if STATE["roots"]:
                do_scan(STATE["roots"])
                if STATE.get("auto_apply_safe"):
                    apply_safe_core(ids=None, target_root=None)

            # Per-second ticks until next scan
            for _ in range(STATE.get("interval",600)):
                if not STATE["auto"]:
                    break
                _watch_tick()        # run watchdog checks
                time.sleep(1)
        except Exception as e:
            print("auto_loop error:", e)

