def scan_root(root: Path):
    items = []
    for p in root.rglob("*"):
        if p.is_file() and not any(x in p.parts for x in [".venv","node_modules",".git",".parcel-cache",".expo","dist","__pycache__"]):
            try:
                txt = p.read_text(encoding="utf-8", errors="ignore")
            except Exception:
                txt = ""
            items.append({
                "path": str(p.relative_to(root)),
                "size": p.stat().st_size,
                "lines": txt.count("\n")+1 if txt else 0,
                "sha1": sha1_text(txt[:2000]) if txt else None,
                "snippet": txt[:400] if txt else "",
            })
    return items

def build_proposals(files: List[Dict[str,Any]]) -> List[Dict[str,Any]]:
    files_low = { f["path"].lower(): f for f in files }
    props: List[Dict[str,Any]] = []

    # 1) Inline TODOs → tasks
    todo_hits = [f for f in files if "todo" in (f.get("snippet") or "").lower()]
    if todo_hits:
        props.append({
            "id": sha1_text("todo-sweep"),
            "title": "Convert inline TODOs into backlog tasks",
            "impact": "medium",
            "apply": {"type":"instruction","text":"Extract TODOs into structured tasks in Onyx Tasks."},
            "evidence": [{"file": f["path"], "found":"TODO"} for f in todo_hits[:10]]
        })

    # 2) .env.local.example for onboarding
    if ".env.local" not in files_low and ".env.local.example" not in files_low:
        props.append({
            "id": sha1_text("env-example"),
            "title": "Add .env.local.example for easier setup",
            "impact": "low",
            "apply": {"type":"file_create","path":".env.local.example","content":"VITE_API_BASE=http://127.0.0.1:8000\n"},
            "evidence": []
        })

    # 3) .gitignore basics
    if ".gitignore" not in files_low:
        props.append({
            "id": sha1_text("gitignore"),
            "title": "Add a .gitignore for env & build artifacts",
            "impact": "low",
            "apply": {"type":"file_create","path":".gitignore","content":"# Onyx/Mason\n.venv/\n.env*\nnode_modules/\ndist/\n__pycache__/\n*.log\n"},
            "evidence": []
        })

    return props

def _merge_proposals(primary: List[Dict[str,Any]], extra: List[Dict[str,Any]]) -> List[Dict[str,Any]]:
    by_id = {p["id"]: dict(p) for p in primary}
    for e in extra:
        by_id[e["id"]] = dict(e)
    return list(by_id.values())

def do_scan(roots: List[str]):
    all_files = []
    for r in roots:
        root = Path(r)
        if root.exists():
            all_files.extend(scan_root(root))
    STATE["files"] = all_files
    STATE["last_scan"] = time.time()
    base_props = build_proposals(all_files)
    # Merge watchdog proposals so they’re always visible
    STATE["proposals"] = _merge_proposals(base_props, WATCH.get("proposals", []))
    audit_log("scan", {"roots": roots, "files": len(all_files), "proposals": len(STATE["proposals"])})
    return {"ok": True, "count": len(all_files), "proposals": len(STATE["proposals"])}

# -------------------------
# SAFE APPLY (no code edits)
# -------------------------
ALLOWED_APPLY_TYPES: Set[str] = {"file_create"}

def _safe_join(root: Path, rel: str) -> Path:
    p = (Path(root) / rel).resolve()
    if not str(p).startswith(str(Path(root).resolve())):
        raise ValueError("path escapes root")
    return p

def apply_safe_core(ids: Optional[List[str]]=None, target_root: Optional[str]=None) -> Dict[str,Any]:
    applied, skipped = [], []
    roots = [Path(target_root)] if target_root else [Path(r) for r in STATE["roots"]]
    # Prefer onyx-web root for top-level project files
    roots_sorted = sorted(roots, key=lambda p: 0 if "onyx-web" in str(p).lower() else 1)
    idset = set(ids) if ids else {p["id"] for p in STATE["proposals"]}

    for pr in STATE["proposals"]:
        if pr["id"] not in idset:
            continue
        ap = pr.get("apply") or {}
        if ap.get("type") not in ALLOWED_APPLY_TYPES:
            skipped.append({"id": pr["id"], "reason": "type_not_allowed"})
            continue

        rel = ap.get("path"); content = ap.get("content", "")
        if not rel or any(t in rel for t in ["..", ":", "\\..", "/.."]):
            skipped.append({"id": pr["id"], "reason": "bad_path"})
            continue

        last_err = None
        for root in roots_sorted:
            try:
                target = _safe_join(root, rel)
                target.parent.mkdir(parents=True, exist_ok=True)
                if target.exists():
                    skipped.append({"id": pr["id"], "reason": "exists", "path": str(target)})
                else:
                    target.write_text(content, encoding="utf-8")
                    applied.append({"id": pr["id"], "path": str(target)})
                break
            except Exception as e:
                last_err = str(e)
        else:
            skipped.append({"id": pr["id"], "reason": last_err or "no_root"})
    result = {"ok": True, "applied": applied, "skipped": skipped}
    audit_log("apply_safe", result)
    return result

