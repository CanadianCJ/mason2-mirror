def sha1_text(s: str) -> str:
    try:
        return hashlib.sha1(s.encode("utf-8","ignore")).hexdigest()
    except Exception:
        return hashlib.sha1(repr(s).encode("utf-8","ignore")).hexdigest()

def _audit_enabled() -> bool:
    try:
        return bool(BRIDGE.get("cfg",{}).get("audit",{}).get("log_file"))
    except Exception:
        return False

def _audit_path() -> Optional[Path]:
    p = BRIDGE.get("cfg",{}).get("audit",{}).get("log_file")
    return Path(p).resolve() if p else None

def audit_log(kind: str, data: Dict[str,Any]):
    if not _audit_enabled():
        return
    try:
        ap = _audit_path()
        ap.parent.mkdir(parents=True, exist_ok=True)
        with ap.open("a", encoding="utf-8") as f:
            f.write(json.dumps({"ts": time.time(), "kind": kind, "data": data}, ensure_ascii=False) + "\n")
    except Exception as e:
        print("audit_log error:", e)

