# WATCHDOG
# -------------------------
class WatchServiceBody(BaseModel):
    name: str
    url: str
    expect_code: int = 200
    interval_sec: int = 30

class WatchConfigBody(BaseModel):
    services: List[WatchServiceBody] = []

def _watch_tick():
    if not WATCH["services"]:
        return
    now = time.time()
    changed = False
    for svc in WATCH["services"]:
        due = float(svc.get("next_due", 0))
        if now < due:
            continue
        svc["next_due"] = now + int(svc.get("interval_sec", 30))
        code = None
        err = None
        try:
            with httpx.Client(timeout=5.0) as client:
                resp = client.get(svc["url"])
                code = resp.status_code
        except Exception as e:
            err = str(e)

        svc["last_time"] = now
        svc["last_status"] = code
        svc["last_error"] = err

        healthy = (code == int(svc.get("expect_code", 200)))
        pid = sha1_text(f"watch:{svc['name']}:{svc['url']}")
        if healthy:
            svc["consecutive_failures"] = 0
            # remove proposal if it exists
            WATCH["proposals"] = [p for p in WATCH["proposals"] if p["id"] != pid]
        else:
            svc["consecutive_failures"] = int(svc.get("consecutive_failures", 0)) + 1
            impact = "high" if svc["consecutive_failures"] >= 3 else "medium"
            title = f"Service '{svc['name']}' appears down"
            detail = f"Expected {svc.get('expect_code',200)}, got {code}. Failures: {svc['consecutive_failures']}."
            prop = {
                "id": pid,
                "title": title,
                "impact": impact,
                "apply": {"type":"instruction","text": f"Check {svc['name']} at {svc['url']}. {detail}"},
                "evidence": [{"last_status": code, "last_error": err, "time": now}],
            }
            # upsert
            for i, p in enumerate(WATCH["proposals"]):
                if p["id"] == pid:
                    WATCH["proposals"][i] = prop
                    break
            else:
                WATCH["proposals"].append(prop)
        changed = True
        audit_log("watch_check", {"name": svc["name"], "url": svc["url"], "status": code, "error": err})

    if changed:
        # merge into STATE without losing existing file-based proposals
        STATE["proposals"] = _merge_proposals(STATE.get("proposals", []), WATCH.get("proposals", []))

